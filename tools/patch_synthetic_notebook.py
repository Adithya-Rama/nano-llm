def new_code_cell(source: str) -> dict:
    return {
        "cell_type": "code",
        "execution_count": None,
        "metadata": {},
        "outputs": [],
        "source": src_lines(source),
    }


def main() -> None:
    nb = json.loads(NB_PATH.read_text(encoding="utf-8"))
    cells = nb["cells"]
    joined = lambda: "".join("".join(c.get("source", [])) for c in cells)

    t3_idx = None
    for i, c in enumerate(cells):
        if MARKER_T3 in "".join(c.get("source", [])):
            t3_idx = i
            break
    if t3_idx is None:
        raise SystemExit(f"Could not find T3 cell with {MARKER_T3!r}")

    if "synthetic_stories_gptoss120b.json" not in joined():
        cells.insert(t3_idx, new_code_cell(CELL_SYN_PREP))
        t3_idx += 1

    if "T3_SYN_CONFIG" not in joined():
        cells.insert(t3_idx + 1, new_code_cell(CELL_T3_SYN))

    hf_idx = None
    for i, c in enumerate(cells):
        text = "".join(c.get("source", []))
        if "§3.4 HuggingFace" in text and "SUBMIT_DIR" in text and "T3_DIR" in text:
            hf_idx = i
            break
    if hf_idx is None:
        raise SystemExit("Could not find HuggingFace upload cell (§3.4)")

    if "Checkpoint Comparison" not in joined():
        cells.insert(hf_idx, new_code_cell(CELL_COMPARE))

    for c in cells:
        text = "".join(c.get("source", []))
        if "§3.4 HuggingFace" not in text or "T3_DIR" not in text:
            continue
        new_src = []
        for line in c["source"]:
            if line.strip() == "T3_DIR     = 'out-t3-best'":
                new_src.append(
                    "# USE_DIR set by comparison cell above; default to out-t3-best\n"
                )
                new_src.append(
                    "T3_DIR = USE_DIR if 'USE_DIR' in dir() else 'out-t3-best'\n"
                )
            else:
                new_src.append(line)
        c["source"] = new_src
        break

    old_t4 = (
        'print(f"\\n  ── Stage 2: Plain ROCStories fine-tune ───────────────────────────────")\n'
        'print(f"  Dataset  : data/rocstories_plain/")\n'
        'print(f"  Config   : {T4_S2_CONFIG}")\n'
        'print(f"  Out dir  : {T4_S2_OUT_DIR}")\n'
        'print(f"  Init     : resume (from Stage 1 best checkpoint)")\n'
        'print(f"  Steps    : 32,000")\n'
        'print(f"  ETA      : ~2.5 hrs on A100  (plain ROCStories; see train_t4_finetune.py)\\n")\n'
        "\n"
        "rc2 = run_streaming(\n"
    )
    new_t4 = (
        'print(f"\\n  ── Stage 2: Synthetic ROCStories fine-tune ───────────────────────────────")\n'
        'print(f"  Dataset  : data/rocstories_synthetic/  (~22.4M tokens, gpt-oss-120B KD)")\n'
        'print(f"  Config   : config/train_t4_finetune.py")\n'
        'print(f"  Out dir  : out-t4-arena")\n'
        'print(f"  Init     : resume (from Stage 1 best — step 25K)")\n'
        'print(f"  Steps    : 30,000  (25K Stage1 + 5K fine-tune = 5K new steps)")\n'
        'print(f"  ETA      : ~35 min on A100")\n'
        "\n"
        "# Verify synthetic dataset exists before starting\n"
        "if not os.path.exists('data/rocstories_synthetic/train.bin'):\n"
        '    print("⚠  data/rocstories_synthetic/train.bin not found!")\n'
        '    print("   Run §2.5 cell first to build the synthetic dataset.")\n'
        '    print("   Stopping — do not proceed without synthetic data.")\n'
        "    raise SystemExit(1)\n"
        'print("✓ Synthetic dataset verified")\n'
        "print()\n"
        "\n"
        "rc2 = run_streaming(\n"
    )

    for c in cells:
        src = c.get("source", [])
        full = "".join(src)
        if "T4_S2_CONFIG = 'config/train_t4_finetune.py'" in full and old_t4 in full:
            full = full.replace(old_t4, new_t4)
            c["source"] = src_lines(full)
            break

    for c in cells:
        full = "".join(c.get("source", []))
        if "§5 — FINAL SUMMARY" not in full or "ALL_RUNS" not in full:
            continue
        full = full.replace(
            "    ('out-t3-best',       'T3: Best Submission',   '#D85A30', True,  True,  True,  True ),\n",
            "    ('out-t3-best',       'T3 (orig): Best',       '#D85A30', True,  True,  True,  True ),\n"
            "    ('out-t3-synthetic',  'T3 (synth): gpt-oss KD', '#E67E22', True,  True,  True,  True ),\n",
        )
        full = full.replace(
            "    ('out-t4-arena',      'T4: Arena (124M)',        '#0F6E56', True,  True,  True,  True ),\n",
            "    ('out-t4-arena',      'T4: Arena (Stage 2: gpt-oss-120B synthetic)', '#0F6E56', True,  True,  True,  True ),\n",
        )
        c["source"] = src_lines(full)
        break

    NB_PATH.write_text(json.dumps(nb, indent=1, ensure_ascii=False) + "\n", encoding="utf-8")
    print("Patched", NB_PATH)


if __name__ == "__main__":
    main()
