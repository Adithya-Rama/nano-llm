MARKER_T3 = "T3_CONFIG  = 'config/train_t3_best.py'"


def src_lines(code: str) -> list[str]:
    if not code.endswith("\n"):
        code = code + "\n"
    parts = code.split("\n")
    return [p + "\n" for p in parts[:-1]] + ([parts[-1]] if parts[-1] != "" else [])


